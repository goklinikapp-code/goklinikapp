export type TranslationKey = keyof (typeof translations)['en']

export function getLocaleForLanguage(language: SupportedLanguage): string {
  return localeByLanguage[language] || 'en-US'
}

export function normalizeLanguage(value?: string | null): SupportedLanguage {
  const supportedLanguage = parseSupportedLanguage(value)
  if (supportedLanguage) {
    return supportedLanguage
  }
  return 'en'
}

function parseSupportedLanguage(value?: string | null): SupportedLanguage | null {
  if (!value) return null
  const normalized = value.trim().toLowerCase().split(/[-_]/)[0]
  if (SUPPORTED_LANGUAGES.includes(normalized as SupportedLanguage)) {
    return normalized as SupportedLanguage
  }
  return null
}

export function normalizeCurrency(value?: string | null): SupportedCurrency {
  if (!value) return 'USD'
  const normalized = value.trim().toUpperCase()
  if (SUPPORTED_CURRENCIES.includes(normalized as SupportedCurrency)) {
    return normalized as SupportedCurrency
  }
  return 'USD'
}

export function detectLanguageFromBrowser(): SupportedLanguage {
  if (typeof window === 'undefined') {
    return 'en'
  }
  const browserLanguages = [...(window.navigator.languages || []), window.navigator.language]
  for (const language of browserLanguages) {
    const resolved = parseSupportedLanguage(language)
    if (resolved) {
      return resolved
    }
  }
  return 'en'
}

export function resolveDefaultCurrency(language: SupportedLanguage): SupportedCurrency {
  if (language === 'tr') {
    return 'TRY'
  }
  if (language === 'en') {
    return 'USD'
  }
  if (euroLanguages.has(language)) {
    return 'EUR'
  }
  return 'USD'
}

export function t(language: SupportedLanguage, key: TranslationKey): string {
  const selected = translations[language] as Record<string, string> | undefined
  const fallback = translations.en as Record<string, string>
  return selected?.[key] || fallback[key] || key
}
